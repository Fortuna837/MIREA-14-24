# Исходный список с чередованием букв и цифр
li = ['a', '1', 'b', '2', 'c', '3']

# Создаем список только с буквами: берем каждый второй элемент, начиная с индекса 0
li_letters = li[::2]

# Создаем список только с цифрами: берем каждый второй элемент, начиная с индекса 1  
li_numbers = li[1::2]

# Удаляем исходный список для освобождения памяти
del li

# Выводим результат: список букв
print('Буквы:', li_letters)

# Выводим результат: список цифр
print('Цифры:', li_numbers)
