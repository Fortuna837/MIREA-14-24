// Подключаем библиотеку для ввода и вывода данных
#include <iostream>
// Подключаем библиотеку алгоритмов (содержит sort и unique)
#include <algorithm>
// Подключаем библиотеку для работы с векторами (динамическими массивами)
#include <vector>
// Используем стандартное пространство имен для удобства
using namespace std;

// Основная функция программы
int main() {
    // Создаем вектор и инициализируем его значениями с дубликатами
    vector<int> vec = {1, 2, 2, 3, 4, 4, 5};
    
    // Сортируем вектор по возрастанию (алгоритм unique требует отсортированных данных)
    sort(vec.begin(), vec.end());
    
    // Удаляем дубликаты из вектора:
    // unique() перемещает уникальные элементы в начало и возвращает итератор на новый конец
    // erase() удаляет все элементы от нового конца до старого конца вектора
    vec.erase(unique(vec.begin(), vec.end()), vec.end());
    
    // Используем range-based for loop для перебора всех элементов вектора
    for (int num : vec) {
        // Выводим каждый элемент через пробел
        cout << num << " ";
    }
    
    // Завершаем программу с кодом успешного выполнения
    return 0;
}
