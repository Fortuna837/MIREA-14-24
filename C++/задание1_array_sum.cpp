// Подключаем библиотеку для ввода/вывода
#include <iostream>
// Используем стандартное пространство имен для упрощения кода
using namespace std;

// Главная функция программы
int main() {
    // Создаем массив целых чисел с тестовыми данными
    int arr[] = {1, 2, 3, 4, 5};
    // Инициализируем переменную для хранения суммы (начальное значение 0)
    int sum = 0;
    // Начинаем цикл от 0 до 4 (5 итераций)
    for (int i = 0; i < 5; i++) {
        // Добавляем текущий элемент массива к сумме
        sum += arr[i];
    }
    // Выводим результат на экран
    cout << "Сумма: " << sum << endl;
    // Завершаем программу с кодом 0 (успешное выполнение)
    return 0;
}
